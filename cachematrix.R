## Write a short comment describing this function
## The function takes a variable "x" which should be a matrix
## The function outputs 4 functions as a result to as a list (if you look at variable that uses 
## this function it will be a list of 4 elements which are functions).
## if you do not want to re-run the function, which you can, you can run the "set" which is a function
## that will adjust the matrix to the value(matrix) you give in "set" function back to x and also reset 
## inverse_x to null at parent level, so that when you run solve it will have to solve it again and not use cache

makeCacheMatrix <- function(x = matrix()) {
    inverse_x <- NULL
    set <- function(y) {
        x <<- y
        inverse_x <<- NULL
    }
    get <- function() x
    setinverse <- function(inverse) inverse_x <<- inverse
    getinverse <- function() inverse_x
    list(set = set, get = get,
        setinverse = setinverse,
        getinverse = getinverse)
}


## Write a short comment describing this function
## This function is trying to solve for inverse of 'x'
## It checks first if the inverse_x is NOT NULL, if so it outputs message "getting cached data"
## and returns the value of inverse_x as an output of the function.
## If inverse_x is NULL it runs the rest of the code and solve for inverse_x using the makeCacheMatrix 
## functions. Then returns inverse_x that is calculated (note: no message is generated)

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  inverse_x <- x$getinverse()
  if(!is.null(inverse_x)) {
    message("getting cached data")
    return(inverse_x)
  }
  data <- x$get()
  inverse_x <- solve(data, ...)
  x$setinverse(inverse_x)
  inverse_x
}

